
def suma(n1: int, n2: int):
    """Suma dos numeros enteros y devuelve el resultado
    
        args:
            n1: primer numero a sumar
            n2: segundo numero a sumar
            
        return: resultado de la suma de n1 y n2
    """
    return n1 + n2

def diferencia(n1, n2) -> int:
    return n1 - n2